package com.ustglobal.beanobject;

public class Student {

	private int id;
	private String name;
	private int rollno;

	public Student() {

	}

	public void setId(int id) {
		this.id = id;
	}

	public void setName(String name) {
		this.name = name;
	}

	public void setRollno(int rollno) {
		this.rollno = rollno;
	}

	public int getId() {
		return id;
	}

	public String getName() {
		return name;
	}

	public int getRollno() {
		return rollno;
	}
}
